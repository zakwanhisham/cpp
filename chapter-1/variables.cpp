#include <iostream>
#include <string>
using namespace std;

int main() {
  // declaring variable
  int a, b;
  int result;

  // process
  a = 5;
  b = 2;
  a += 1;
  result = a - b;

  // print out the result
  cout << result << endl;

  // declare string
  string mystring;
  mystring = "This is a string";
  cout << mystring << endl;
  mystring = "This is a different string";
  cout << mystring << endl;
  // terminate program
  return 0;
}
